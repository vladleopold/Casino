function slugifyDemoLookupValue(value?: string | null) {
  if (typeof value !== "string") {
    return undefined;
  }

  const normalized = value
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

  return normalized || undefined;
}

function normalizeDemoLookupSlug(slug: string) {
  return slug
    .replace(/^\/+|\/+$/g, "")
    .replace(/^game\//, "")
    .replace(/^bonus-/, "")
    .replace(/-home$/, "")
    .replace(/-discovery$/, "")
    .trim();
}

export function buildSlotcityDemoLookupKeys(slug: string, name?: string | null) {
  const terms = new Set<string>();
  const normalizedName = slugifyDemoLookupValue(name);
  const normalizedSlug = slugifyDemoLookupValue(slug);
  const cleanedSlug = slugifyDemoLookupValue(normalizeDemoLookupSlug(slug));

  if (normalizedName) {
    terms.add(normalizedName);
  }

  if (cleanedSlug) {
    terms.add(cleanedSlug);
  }

  if (normalizedSlug) {
    terms.add(normalizedSlug);
  }

  return Array.from(terms);
}

